"""
Surgical Planning Report Generator

Generates structured clinical reports for fracture reduction plans.
These reports are designed for surgeon review and become part of the
surgical planning record (for QA and regulatory documentation).

Report sections:
1. Patient & Case Summary
2. CT Quality Assessment
3. Segmentation Results
4. Fracture Pattern Analysis
5. Reduction Plan Details
6. Occlusal Analysis
7. Hardware Recommendations
8. Risk Assessment
9. Surgeon Action Items

Output: Structured Markdown (can be rendered to PDF for clinical use)
"""

from __future__ import annotations

from dataclasses import dataclass, field
from datetime import datetime, timezone
from typing import Any, Dict, List, Optional

import numpy as np

from services.evaluation.plan_evaluator import (
    CondylarAssessment,
    FragmentAlignmentMetrics,
    HardwareRecommendation,
    OcclusionMetrics,
    PlanEvaluation,
    PlanGrade,
    SymmetryMetrics,
)


@dataclass
class CaseSummary:
    """Minimal case info for report header."""
    case_number: str
    case_type: str
    fracture_classification: str
    planned_procedure: str
    surgeon_name: str
    report_date: datetime = field(default_factory=lambda: datetime.now(timezone.utc))


@dataclass
class CTQualitySummary:
    """CT scan quality info for the report."""
    slice_thickness_mm: float
    pixel_spacing_mm: float
    quality_grade: str  # A/B/C/F
    quality_score: float
    warnings: List[str] = field(default_factory=list)


@dataclass
class SegmentationSummary:
    """Segmentation result summary for the report."""
    model_name: str
    model_version: str
    structures_segmented: List[str]
    overall_confidence: float
    fragment_count: int
    processing_time_seconds: float


class ReportGenerator:
    """
    Generates a comprehensive surgical planning report in Markdown format.

    The report is structured for clinical review and follows standard
    surgical planning documentation practices for CMF surgery.
    """

    def generate(
        self,
        case: CaseSummary,
        ct_quality: CTQualitySummary,
        segmentation: SegmentationSummary,
        evaluation: PlanEvaluation,
        plan_metadata: Optional[Dict[str, Any]] = None,
    ) -> str:
        """
        Generate a complete surgical planning report.

        Returns:
            Markdown-formatted report string
        """
        sections = [
            self._header(case),
            self._disclaimer(),
            self._case_summary(case),
            self._ct_quality_section(ct_quality),
            self._segmentation_section(segmentation),
            self._fracture_analysis(evaluation),
            self._reduction_plan_section(evaluation),
            self._occlusion_section(evaluation.occlusion),
            self._symmetry_section(evaluation.symmetry),
            self._condylar_section(evaluation.condylar),
            self._hardware_section(evaluation.hardware),
            self._risk_assessment(evaluation),
            self._action_items(evaluation),
            self._technical_appendix(evaluation, plan_metadata),
            self._footer(case),
        ]
        return "\n\n".join(sections)

    # ── Report sections ───────────────────────────────────────────────────────

    def _header(self, case: CaseSummary) -> str:
        return f"""# Facial Align — Surgical Planning Report

**Case:** {case.case_number}
**Date:** {case.report_date.strftime('%B %d, %Y %H:%M UTC')}
**Surgeon:** {case.surgeon_name}
**Classification:** {case.fracture_classification}
**Planned Procedure:** {case.planned_procedure}

---"""

    def _disclaimer(self) -> str:
        return """> **IMPORTANT**: This report is generated by an AI-assisted surgical planning
> system and is intended as a decision-support tool only. All surgical
> decisions must be made by the treating surgeon based on clinical judgment,
> physical examination, and review of all available imaging. This system
> has NOT been cleared by the FDA for independent clinical decision-making."""

    def _case_summary(self, case: CaseSummary) -> str:
        return f"""## Case Summary

| Field | Value |
|-------|-------|
| Case Number | {case.case_number} |
| Case Type | {case.case_type} |
| Fracture Classification | {case.fracture_classification} |
| Planned Procedure | {case.planned_procedure} |
| Attending Surgeon | {case.surgeon_name} |
| Report Generated | {case.report_date.strftime('%Y-%m-%d %H:%M UTC')} |"""

    def _ct_quality_section(self, ct: CTQualitySummary) -> str:
        grade_emoji = {"A": "Excellent", "B": "Acceptable", "C": "Marginal", "F": "Inadequate"}
        grade_desc = grade_emoji.get(ct.quality_grade, "Unknown")

        warnings_text = ""
        if ct.warnings:
            warnings_text = "\n\n**Warnings:**\n" + "\n".join(f"- {w}" for w in ct.warnings)

        return f"""## CT Quality Assessment

| Parameter | Value | Standard |
|-----------|-------|----------|
| Slice Thickness | {ct.slice_thickness_mm:.2f} mm | ≤ 1.0 mm for surgical planning |
| Pixel Spacing | {ct.pixel_spacing_mm:.2f} mm | ≤ 0.5 mm preferred |
| Quality Grade | **{ct.quality_grade}** — {grade_desc} | A or B required |
| Quality Score | {ct.quality_score:.0%} | ≥ 80% for planning |{warnings_text}"""

    def _segmentation_section(self, seg: SegmentationSummary) -> str:
        structures_list = "\n".join(f"- {s}" for s in seg.structures_segmented)
        confidence_bar = self._confidence_bar(seg.overall_confidence)

        return f"""## Segmentation Results

**Model:** {seg.model_name} v{seg.model_version}
**Overall Confidence:** {seg.overall_confidence:.0%} {confidence_bar}
**Processing Time:** {seg.processing_time_seconds:.1f} seconds
**Fragments Identified:** {seg.fragment_count}

### Structures Segmented
{structures_list}"""

    def _fracture_analysis(self, evaluation: PlanEvaluation) -> str:
        if not evaluation.fragment_metrics:
            return "## Fracture Pattern Analysis\n\nNo fragment data available."

        rows = []
        for fm in evaluation.fragment_metrics:
            grade_str = fm.grade.value.capitalize()
            rows.append(
                f"| {fm.fragment_id} | {fm.parent_structure} | "
                f"{fm.translation_magnitude_mm:.1f} mm | "
                f"{fm.rotation_magnitude_degrees:.1f}° | "
                f"{fm.mean_surface_distance_mm:.2f} mm | "
                f"**{grade_str}** |"
            )

        table = "\n".join(rows)

        return f"""## Fracture Pattern Analysis

### Fragment Assessment

| Fragment | Structure | Displacement | Rotation | Mean Error | Grade |
|----------|-----------|-------------|----------|------------|-------|
{table}

**Total fragments:** {len(evaluation.fragment_metrics)}"""

    def _reduction_plan_section(self, evaluation: PlanEvaluation) -> str:
        grade_str = evaluation.overall_grade.value.upper()
        confidence_bar = self._confidence_bar(evaluation.overall_confidence)

        return f"""## Reduction Plan

**Overall Grade:** **{grade_str}**
**Overall ML Confidence:** {evaluation.overall_confidence:.0%} {confidence_bar}
**Evaluation Time:** {evaluation.evaluation_time_ms} ms

### Grade Interpretation

| Grade | Meaning |
|-------|---------|
| EXCELLENT | All metrics within ideal clinical range |
| GOOD | Minor deviations, clinically acceptable |
| ACCEPTABLE | Within surgical tolerance |
| MARGINAL | Borderline — requires careful surgeon review |
| POOR | Major deviations — modify plan before proceeding |"""

    def _occlusion_section(self, occ: OcclusionMetrics) -> str:
        rows = []
        if occ.overjet_mm is not None:
            status = "Normal" if 1.0 <= occ.overjet_mm <= 3.0 else "Abnormal"
            rows.append(f"| Overjet | {occ.overjet_mm:.1f} mm | 1.0 – 3.0 mm | {status} |")
        if occ.overbite_mm is not None:
            status = "Normal" if 2.0 <= occ.overbite_mm <= 4.0 else "Abnormal"
            rows.append(f"| Overbite | {occ.overbite_mm:.1f} mm | 2.0 – 4.0 mm | {status} |")
        if occ.midline_deviation_mm is not None:
            status = "Normal" if abs(occ.midline_deviation_mm) <= 1.0 else "Abnormal"
            rows.append(f"| Midline Deviation | {occ.midline_deviation_mm:.1f} mm | ≤ 1.0 mm | {status} |")
        if occ.molar_relationship is not None:
            status = "Normal" if occ.molar_relationship == "Class_I" else "Deviation"
            rows.append(f"| Molar Relationship | {occ.molar_relationship} | Class I | {status} |")
        if occ.cant_degrees is not None:
            status = "Normal" if abs(occ.cant_degrees) <= 2.0 else "Abnormal"
            rows.append(f"| Occlusal Cant | {occ.cant_degrees:.1f}° | ≤ 2.0° | {status} |")

        if not rows:
            return """## Dental Occlusion

Occlusal metrics not available (dental arch data not provided or evaluation pending)."""

        table = "\n".join(rows)
        return f"""## Dental Occlusion

| Metric | Measured | Normal Range | Status |
|--------|----------|-------------|--------|
{table}

**Occlusion Grade:** {occ.grade.value.capitalize()}"""

    def _symmetry_section(self, sym: SymmetryMetrics) -> str:
        score_pct = sym.overall_symmetry_score * 100

        rows = []
        if sym.mandible_asymmetry_mm > 0:
            rows.append(f"| Mandible | {sym.mandible_asymmetry_mm:.1f} mm | ≤ 3.0 mm |")
        if sym.maxilla_asymmetry_mm > 0:
            rows.append(f"| Maxilla | {sym.maxilla_asymmetry_mm:.1f} mm | ≤ 3.0 mm |")
        if sym.zygomatic_asymmetry_mm > 0:
            rows.append(f"| Zygoma | {sym.zygomatic_asymmetry_mm:.1f} mm | ≤ 2.5 mm |")
        if sym.orbital_asymmetry_mm > 0:
            rows.append(f"| Orbit | {sym.orbital_asymmetry_mm:.1f} mm | ≤ 2.0 mm |")
        if sym.orbital_volume_diff_cc is not None:
            rows.append(f"| Orbital Volume Difference | {sym.orbital_volume_diff_cc:.1f} cc | ≤ 1.5 cc |")

        table = "\n".join(rows) if rows else "| No bilateral structures found | — | — |"

        return f"""## Facial Symmetry

**Overall Symmetry Score:** {score_pct:.0f}% ({sym.grade.value.capitalize()})

| Region | Asymmetry | Threshold |
|--------|-----------|-----------|
{table}"""

    def _condylar_section(self, cond: CondylarAssessment) -> str:
        seating_str = "Achieved" if cond.bilateral_seating_achieved else "NOT achieved"

        return f"""## Condylar Assessment

| Parameter | Left | Right | Threshold |
|-----------|------|-------|-----------|
| Displacement | {cond.left_condyle_displacement_mm:.1f} mm | {cond.right_condyle_displacement_mm:.1f} mm | ≤ 2.0 mm |
| Rotation | {cond.left_condyle_rotation_degrees:.1f}° | {cond.right_condyle_rotation_degrees:.1f}° | ≤ 5.0° |

**Bilateral Seating:** {seating_str}"""

    def _hardware_section(self, hardware: List[HardwareRecommendation]) -> str:
        if not hardware:
            return "## Hardware Recommendations\n\nNo hardware recommendations generated."

        sections = []
        for hw in hardware:
            considerations = "\n".join(f"  - {c}" for c in hw.special_considerations)
            sections.append(f"""### {hw.fragment_id}
- **System:** {hw.plate_system or 'TBD'}
- **Type:** {hw.hardware_type.replace('_', ' ').title()}
- **Screw Count:** {hw.screw_count_min}–{hw.screw_count_max}
- **Screw Length:** {hw.screw_length_mm or 'TBD'} mm
- **Profile:** {hw.plate_profile or 'standard'}
- **Considerations:**
{considerations}""")

        body = "\n\n".join(sections)
        return f"""## Hardware Recommendations

{body}"""

    def _risk_assessment(self, evaluation: PlanEvaluation) -> str:
        warnings_text = "\n".join(f"- **Warning:** {w}" for w in evaluation.warnings) if evaluation.warnings else "- No warnings"
        recs_text = "\n".join(f"- {r}" for r in evaluation.recommendations) if evaluation.recommendations else "- None"
        contra_text = "\n".join(f"- **{c}**" for c in evaluation.contraindications) if evaluation.contraindications else "- None identified"

        return f"""## Risk Assessment

### Warnings
{warnings_text}

### Recommendations
{recs_text}

### Contraindications
{contra_text}"""

    def _action_items(self, evaluation: PlanEvaluation) -> str:
        items = [
            "Review all fragment transforms in the 3D viewer",
            "Verify occlusal relationship matches clinical exam",
            "Confirm condylar seating bilaterally",
            "Review hardware placement against planned skin incisions",
            "Approve or modify plan before proceeding to splint fabrication",
        ]

        if evaluation.overall_grade in (PlanGrade.MARGINAL, PlanGrade.POOR):
            items.insert(0, "**CRITICAL: Plan grade is below acceptable threshold. Review all flagged metrics.**")

        if any(fm.model_confidence < 0.7 for fm in evaluation.fragment_metrics):
            items.insert(1, "Manually verify fragments with low ML confidence")

        numbered = "\n".join(f"{i+1}. {item}" for i, item in enumerate(items))
        return f"""## Surgeon Action Items

{numbered}"""

    def _technical_appendix(
        self, evaluation: PlanEvaluation, metadata: Optional[Dict[str, Any]]
    ) -> str:
        fragment_detail = ""
        for fm in evaluation.fragment_metrics:
            fragment_detail += f"""
#### {fm.fragment_id}
- Mean surface distance: {fm.mean_surface_distance_mm:.3f} mm
- Max surface distance: {fm.max_surface_distance_mm:.3f} mm
- Hausdorff distance: {fm.hausdorff_distance_mm:.3f} mm
- RMS surface distance: {fm.rms_surface_distance_mm:.3f} mm
- Translation: {fm.translation_magnitude_mm:.2f} mm
- Rotation: {fm.rotation_magnitude_degrees:.2f}°
- ICP fitness: {fm.icp_fitness:.4f}
- Model confidence: {fm.model_confidence:.4f}
"""

        return f"""## Technical Appendix

### Per-Fragment Detailed Metrics
{fragment_detail}

### Evaluation Parameters
- Evaluation time: {evaluation.evaluation_time_ms} ms
- Overall confidence: {evaluation.overall_confidence:.4f}
- Symmetry score: {evaluation.symmetry.overall_symmetry_score:.4f}"""

    def _footer(self, case: CaseSummary) -> str:
        return f"""---

*Generated by Facial Align v0.1.0 — AI-Assisted Craniofacial Surgical Planning*
*Case: {case.case_number} | Date: {case.report_date.strftime('%Y-%m-%d')}*
*This document is part of the surgical planning record.*"""

    # ── Helpers ────────────────────────────────────────────────────────────────

    @staticmethod
    def _confidence_bar(confidence: float) -> str:
        """Generate a text-based confidence indicator."""
        filled = int(confidence * 10)
        return "█" * filled + "░" * (10 - filled)
